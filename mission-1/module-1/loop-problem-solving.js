// incremental while loop
let num = 1;
while (num <= 20) {
  console.log(num);
  num++;
}

// decremental while loop
let num2 = 20;
while (num2 >= 1) {
  console.log(num2);
  num2--;
}

// calculate sum using while loop
let sum = 0;
let num3 = 11;
while (num3 <= 20) {
  sum = sum + num3;
  num3++;
}

console.log(sum);
